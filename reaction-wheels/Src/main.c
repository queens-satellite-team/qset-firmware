/* USER CODE BEGIN 4 */
void changeMotor(uint8_t vals){
	uint8_t powerBit = vals & 0x1;
	uint8_t speedBits = (vals >> 1) & 0x3;
	uint8_t directionBit = (vals >> 3) & 0x1;

	if(powerBit != 1){
		HAL_GPIO_WritePin(GPIOB, GPIO_PIN_15, GPIO_PIN_RESET);
	}
	else{
		HAL_GPIO_WritePin(GPIOB, GPIO_PIN_15, GPIO_PIN_SET);

		// ccw rotation
		if(directionBit == 1){
			HAL_GPIO_WritePin(GPIOC, GPIO_PIN_9, GPIO_PIN_SET);

		}
		else{
			HAL_GPIO_WritePin(GPIOC, GPIO_PIN_9, GPIO_PIN_RESET);
		}

		switch(speedBits){
		 	case 0:
		 		// 25%
		 		__HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_4, 77);
				break;
		 	case 1:
		 		// 50%
		 		__HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_4, 128);
				break;
		 	case 2:
		 		// 75%
		 		__HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_4, 179);
				break;
		 	case 3:
		 		// 100%
		 		__HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_4, 230);
				break;

		}
	}
}
